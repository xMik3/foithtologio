USE universityBranch;

CREATE TABLE Students(
    SID INT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(30) NOT NULL,
    SURNAME VARCHAR(30) NOT NULL,
    SEMESTER INT NOT NULL,
    PASSWORD VARCHAR(30) NOT NULL,
    AVAILCOURSE INT NOT NULL,
    PRIMARY KEY(SID)
);

