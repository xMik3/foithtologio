package models.general;

public class Course {

    private String CID;
    private String NAME;
    private int SEMESTER;
    private String TID;

    public String getCID() {
        return this.CID;
    }

    public String getNAME() {
        return this.NAME;
    }

    public int getSEMESTER() {
        return this.SEMESTER;
    }

    public String getTID() {
        return this.TID;
    }
}
